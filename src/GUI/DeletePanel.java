    private void btnConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmActionPerformed
        // TODO add your handling code here:
        timer = new Timer(100, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                  i++;
                    if(i==1){
                        dispose();//CLOSE FRAME
                        delLoad.setVisible(true);
                        delLoad.lbldelLoad.setText("Processing . . . ");
                    }//END IF
                    
                    if (i==15){
                    delLoad.lbldelLoad.setText("Deleting Product ID : "+ productId + " . . .");
                    }//END IF
                    
                    
                    if(i==50){
                       delLoad.dispose();  
                      //CREATE OBJECT DELETE PRODUCT AND PASS PRODUCT ID VALUE
                      deleteProduct dp = new deleteProduct(productId);
                      delStatus = true;
                      playExclamation();
                      new msgBox("Product with ID : "+productId+"\nhas been successfully deleted !","Product Deleted").showinfoMsg();  
                    }//END IF
                    
                    if(i>51){
                        timer.stop();
                        i=0;
                    }//END IF
            }//END PUBLIC VOID
        });
        timer.start();
    }//GEN-LAST:event_btnConfirmActionPerformed

