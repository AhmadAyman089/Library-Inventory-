     public void loadData(int n){
      n=n-1;
      String strPath = "C:/Inventory.txt";
      String line;
      try (BufferedReader br = new BufferedReader(new FileReader(strPath))) {
          for (int i = 0; i < n; i++)
              br.readLine();
          line = br.readLine();
          System.out.println(line);
          String[] myData = line.split(",");
          System.out.println("Product ID     : " + myData[0]);
          System.out.println("Product Type   : " + myData[1]);
          System.out.println("Product Name   : " + myData[2]);
          System.out.println("Product Qty    : " + myData[3]);
          System.out.println("Product Price  : " + myData[4]);
          
          setTitle("Remove Product with ID : " + myData[0] + " ?");
          ctprodId.setText(myData[0]);
          ctprodType.setText(myData[1]);
          ctprodName.setText(myData[2]);
          ctprodQty.setText(myData[3]);
          ctprodPrice.setText(myData[4]);
          
          int Qty = Integer.parseInt(myData[3]);
          double price = Double.parseDouble(myData[4]);
          invValue = Qty*price;
          proqty = Qty;
          }
      catch(IOException e){
        System.out.println(e);
      }//END TRY CATCH
        
    }//END METHOD
     
